# print('Solution B AEP:  {:.3f} GWh'.format(aep[71] / 1e9))
# print('Solution A Time:     {:.2f} s'.format(time[19]))
# print('Solution B Time:     {:.2f} s'.format(time[71]))
print("================================")
plt.show()